public class UnitConverter2{
    public static double convertInchesIntoMeters(double inches){
        return inches*0.0254;
    }

    public static double convertMetersIntoInches(double meters){
        return meters*39.3701;
    }

    public static double convertFeetIntoYards(double feets){
        return feets/3;
    }

    public static double convertYardsIntoFeet(double yards){
        return 3*yards;
    }

    public static double convertInchesIntoCentimeters(double inches){
        return inches*2.54;
    }
}